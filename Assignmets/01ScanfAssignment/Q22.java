// Q22. Pond: 50×30×2 m. Find capacity in cubic meter.

class Test {
    public static void main(String args[]) {
        int length = 50;
        int breadth = 30;
        int depth = 2;

        int volume = length * breadth * depth;

        System.out.println("Capacity: " + volume + " m^3");
    }
}
